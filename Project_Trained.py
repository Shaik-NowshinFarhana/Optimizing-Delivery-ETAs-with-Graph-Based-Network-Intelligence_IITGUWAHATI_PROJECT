data["delay"] = data["actual_time"] - data["osrm_time"]

data["delay_ratio"] = data["actual_time"] / data["osrm_time"]

data["sla_breach"] = np.where(data["delay_ratio"] > 1.20, 1, 0)

data["within_15_percent_osrm"] = np.where(
    abs(data["actual_time"] - data["osrm_time"]) <= 0.15 * data["actual_time"],
    1,
    0
)

data.head()



edge_data = data.groupby(
    ["source_name", "destination_name", "route_type"]
).agg(
    median_delay_ratio=("delay_ratio", "median"),
    avg_delay_ratio=("delay_ratio", "mean"),
    total_delay=("delay", "sum"),
    avg_actual_time=("actual_time", "mean"),
    avg_osrm_time=("osrm_time", "mean"),
    trip_count=("trip_uuid", "nunique"),
    sla_breach_count=("sla_breach", "sum")
).reset_index()

edge_data.head()

G = nx.DiGraph()

for _, row in edge_data.iterrows():
    G.add_edge(
        row["source_name"],
        row["destination_name"],
        weight=row["median_delay_ratio"],
        route_type=row["route_type"],
        avg_delay_ratio=row["avg_delay_ratio"],
        total_delay=row["total_delay"],
        trip_count=row["trip_count"],
        sla_breach_count=row["sla_breach_count"]
    )

print("Number of hubs/nodes:", G.number_of_nodes())
