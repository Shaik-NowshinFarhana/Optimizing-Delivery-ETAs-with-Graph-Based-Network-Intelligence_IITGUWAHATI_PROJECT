source_delay = data.groupby("source_name")["delay"].sum().reset_index()
source_delay.columns = ["hub", "source_delay"]

destination_delay = data.groupby("destination_name")["delay"].sum().reset_index()
destination_delay.columns = ["hub", "destination_delay"]

source_sla = data.groupby("source_name")["sla_breach"].sum().reset_index()
source_sla.columns = ["hub", "source_sla_breaches"]

destination_sla = data.groupby("destination_name")["sla_breach"].sum().reset_index()
destination_sla.columns = ["hub", "destination_sla_breaches"]

hub_df = hub_df.merge(source_delay, on="hub", how="left")
hub_df = hub_df.merge(destination_delay, on="hub", how="left")
hub_df = hub_df.merge(source_sla, on="hub", how="left")
hub_df = hub_df.merge(destination_sla, on="hub", how="left")

hub_df.fillna(0, inplace=True)

hub_df["total_delay_contribution"] = (
    hub_df["source_delay"] + hub_df["destination_delay"]
)

hub_df["total_sla_breaches"] = (
    hub_df["source_sla_breaches"] + hub_df["destination_sla_breaches"]
)

# Normalize values between 0 and 1
scaler = MinMaxScaler()

score_features = [
    "betweenness_centrality",
    "degree_centrality",
    "total_delay_contribution",
    "total_sla_breaches"
]

normalized_values = scaler.fit_transform(hub_df[score_features])

hub_df[[
    "norm_betweenness",
    "norm_degree",
    "norm_delay",
    "norm_sla"
]] = normalized_values

# Final weighted bottleneck score
hub_df["bottleneck_score"] = (
    0.35 * hub_df["norm_betweenness"] +
    0.25 * hub_df["norm_degree"] +
    0.25 * hub_df["norm_delay"] +
    0.15 * hub_df["norm_sla"]
)

